    var allButtons = document.querySelectorAll(".p-eye-menu__modal-btn");
    for (var i = 0; i < allButtons.length; i++) {
      allButtons[i].addEventListener("click", onButtonClick);
    }


    // 閉じるボタンをクリックしたらモーダルを閉じる
    for (var i = 0; i < span.length; i++) {
      (function(i) {
        span[i].onclick = function() {
          for (var j = 0; j < modal.length; j++) {
            (function(j) {
              if (scrollbarWidth !== 0) {
                modal[j].querySelector("div").style.left = "calc(50% + " + (scrollbarWidth / 2) + "px)";
              }
              modal[j].classList.remove('is-visible');

              setTimeout(function() {
                modal[j].querySelector("div").style.top = "";
                modal[j].querySelector("div").style.left = "";
                modal[j].querySelector("div").style.transform = "";
              }, 500);
            })(j);
          }
          document.documentElement.classList.remove("is-modal-opened");

          // headerのpadding-rightをリセット
          // body.style.marginRight = "";
          // header.style.paddingRight = "";
          document.getElementById('pagetop').style.right = "";
        };
      })(i);
    }



    // モーダルの外側をクリックしたらモーダルを閉じる
    window.onclick = function(event) {
      for (var i = 0; i < modal.length; i++) {
        if (event.target == modal[i]) {
          if (scrollbarWidth !== 0) {
            modal[i].querySelector("div").style.left = "calc(50% + " + (scrollbarWidth / 2) + "px)";
          }
          modal[i].classList.remove('is-visible');


          setTimeout(function() {
            modal[i].querySelector("div").style.top = "";
            modal[i].querySelector("div").style.left = "";
            modal[i].querySelector("div").style.transform = "";
          }, 500);

          document.documentElement.classList.remove("is-modal-opened");


          // headerのpadding-rightをリセット
          // body.style.marginRight = "";
          // header.style.paddingRight = "";
          document.getElementById('pagetop').style.right = "";

          break;
        }
      }
    }
