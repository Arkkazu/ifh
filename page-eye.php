      // `.modal-curri__cont` と `.modal-recruit` の要素を取得
      var modalCurriCont = document.querySelector(`#${targetModalId} > div`);
      var modalRecruit = document.querySelector("#" + targetModalId);


      // 両方の要素の高さを取得
      var modalCurriContHeight = modalCurriCont.offsetHeight;
      var modalRecruitHeight = modalRecruit.offsetHeight;

      // `.modal-recruit直下div` の高さが `.modal-recruit` の高さより小さい場合
      if (modalCurriContHeight < modalRecruitHeight) {
        modalCurriCont.style.top = "50%";
        modalCurriCont.style.transform = "translate(-50%,-50%)";
      } else {
        modalCurriCont.style.top = "30px";
        modalCurriCont.style.transform = "translateX(-50%);";
      }

    }

