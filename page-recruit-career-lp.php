    // スクロールバーの幅を取得
    function getScrollbarWidth() {
      const div = document.createElement("div");
      div.style.overflow = "scroll";
      document.body.appendChild(div);
      const scrollbarWidth = div.offsetWidth - div.clientWidth;
      div.remove();
      return scrollbarWidth;
    }

    // 共通要素の取得
    const html = document.querySelector("html");
    // const header = document.querySelector("header");
    const modal = document.getElementById("modal");
    const modalContent = document.getElementById("modal-content");

    // モーダルの表示・非表示を切り替える関数
    function toggleModal(isVisible, content = "") {
      const scrollbarWidth = getScrollbarWidth();

      if (isVisible) {
        modalContent.innerHTML = content;
        modal.classList.remove('opacity-0', 'invisible');
        modal.classList.add('opacity-100', 'visible');
        // document.documentElement.classList.add("is-modal-opened");
        document.body.style.overflow = "hidden";

        if (scrollbarWidth !== 0) {
          html.style.marginRight = `${scrollbarWidth}px`;
          // header.style.paddingRight = `${scrollbarWidth}px`;
          document.body.style.overflow = "hidden"; // 本文のスクロールを無効化
        }
      } else {
        modal.classList.remove('opacity-100', 'visible');
        modal.classList.add('opacity-0');
        setTimeout(() => modal.classList.add('invisible'), 300);

        // document.documentElement.classList.remove("is-modal-opened");
        document.body.style.overflow = "";
        html.style.marginRight = "";
        // header.style.paddingRight = "";
        document.body.style.overflow = ""; // 本文のスクロールを元に戻す
      }
    }

    // ボタンがクリックされたときの処理
    function onButtonClick(event) {
      const clickedButton = event.currentTarget;
      const contentHtml = clickedButton.dataset.modalHtml || "";
      toggleModal(true, contentHtml);
    }

    // 閉じるボタンをクリックしたときの処理
    function onCloseButtonClick() {
      toggleModal(false);
    }

    // モーダルの外側をクリックしたときの処理
    function onModalOutsideClick(event) {
      if (event.target === modal) {
        toggleModal(false);
      }
    }

    // イベントリスナーを設定
    document.querySelectorAll(".js-modal-trigger").forEach(button => {
      button.addEventListener("click", onButtonClick);
    });

    document.querySelector(".js-modal-close").addEventListener("click", onCloseButtonClick);
    window.addEventListener("click", onModalOutsideClick);
  </script>

